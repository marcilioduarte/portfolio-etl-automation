# Criando função pra checar se já existe o arquivo no diretório

def checadir(pasta, arquivo):
    files = []
    for filenames in walk(pasta):
        files.extend(filenames)
        break
    if arquivo in files:
        print(f"O arquivo {arquivo} já existe. Indo para o próximo!\n")
        return True
    else:
        return False

